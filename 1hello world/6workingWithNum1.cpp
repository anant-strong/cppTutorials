#include <iostream>
using namespace std;

int main()
{
    double num = 5.5;
    cout << -0.4532 << endl;
    cout << 5 + 7 << endl;
    cout << 5 - 7 << endl;
    cout << 5 / 7 << endl;
    cout << 5 * 7 << endl;
    cout << 7 % 5 << endl;
    num++;       //this is gonna add one to num value
    cout << num << endl; 
    num--;       //this is gonna subtract one to num value
    cout << num << endl;
    num += 80;   //this is gonna add 80 to num value    
    cout << num << endl;
    num -= 5;    //this is gonna subtract 5 from num value
    cout << num << endl;
    num *= 2;    //this is gonna multiply 2 to num value
    cout << num << endl;
    num /= 5;    //this is gonna divide num value by 5
    cout << num << endl;
    cout << 5.5 + 9 << endl; // any operation b/w decimal and integer will return integer
    cout << 10/3 << endl; 
    cout << 10/3.0;
     
    return 0;
}